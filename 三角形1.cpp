#include<stdio.h> 
int main()
{int n=0;
scanf("%d",&n); 
for(int k=0;k<n;k++)
{
for(int i=0;i<n-k;i++)
{printf(" ");
}
for(int j=1;j<=2*k-1;j++)
{printf("*");
} printf("\n");
}
for(int k=n;k>=0;k--)
{
for(int i=n-k;i>0;i--)
{printf(" ");
}
for(int j=2*k-1;j>0;j--)
{printf("*");
} printf("\n");
}
}
 
